As mentioned before, mind mapping is a tool, an instrument. In the end it's not about mind mapping, it's all about the things you can do with mind mapping. As you get better and better using the tool it comes a second nature, an extension of you and your thinking. You are not conscious about it or the technicalities anymore. Just as a concert pianist doesn't think about notes or fingers but concentrates on creating music and listening, you can focus 100% on your subject, forgetting all about the mind mapping tool itself. 

To reach the level of a ninja there are a few things to practice. I've divided them here to thinking, emotional and physical skills. You'll get better at all of them by practice.

