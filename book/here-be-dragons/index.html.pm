◊leading-words{Like the} ancient map makers already knew, at the edge of the world lies the realm of dragons and other monsters. The same goes for mind maps. When you do a lot of mind mapping and exploring your thoughts, you'll eventually come to the edge of your inner world. 

You'll encounter new thoughts and thought patterns. Some of them useful, some of them not, some just very confusing. You'll come across old thoughts that you've tried to forget. This can be intimidating, even plain scary. 

