◊h1{Modularity}

◊leading-words{Following} the principle of information hiding leads to a modular structure. You can reuse some maps in many different contexts. Continuing the book writing example, I don't want to mix the methodology of writing with the actual book content. First of all it distracts my focus from the content, but also because by creating a separate map for it I can reuse it. I can link the generic writing map to any other writing related map and find and access it easily for example when writing blog articles.
